void main() async {
  final outputDir = Directory('assets/content/strokes');
  if (!outputDir.existsSync()) {
    outputDir.createSync(recursive: true);
  }

  // Get unique characters (single characters only)
  final uniqueChars =
      hsk14Characters.where((c) => c.length == 1).toSet().toList();

  stdout.writeln('Found ${uniqueChars.length} unique single characters');

  int downloaded = 0;
  int failed = 0;

  for (final char in uniqueChars) {
    final outputFile = File('${outputDir.path}/$char.json');
    if (outputFile.existsSync()) {
      stdout.writeln('Skipping $char (already exists)');
      continue;
    }

    try {
      final url =
          'https://cdn.jsdelivr.net/npm/hanzi-writer-data@2.0/$char.json';
      final client = HttpClient();
      final request = await client.getUrl(Uri.parse(url));
      final response = await request.close();

      if (response.statusCode == 200) {
        final body = await response.transform(utf8.decoder).join();
        final data = jsonDecode(body) as Map<String, dynamic>;

        // Extract medians (simplified stroke paths)
        final medians = data['medians'] as List?;
        if (medians != null && medians.isNotEmpty) {
          final strokes = <List<List<double>>>[];
          for (final median in medians) {
            final points = <List<double>>[];
            for (final point in median as List) {
              final pt = point as List;
              points.add([
                (pt[0] as num).toDouble(),
                (pt[1] as num).toDouble(),
              ]);
            }
            strokes.add(points);
          }

          final output = {
            'character': char,
            'strokes': strokes,
          };

          // Write with explicit UTF-8 encoding
          final fileBytes = utf8.encode(
            const JsonEncoder.withIndent('  ').convert(output),
          );
          outputFile.writeAsBytesSync(fileBytes);
          downloaded++;
          stdout
              .writeln('Downloaded: $char ($downloaded/${uniqueChars.length})');
        } else {
          failed++;
          stdout.writeln('No medians for: $char');
        }
      } else {
        failed++;
        stdout.writeln(
            'Failed to download: $char (status: ${response.statusCode})');
      }
      client.close();
    } catch (e) {
      failed++;
      stdout.writeln('Error downloading $char: $e');
    }

    // Small delay to avoid rate limiting
    if (downloaded % 10 == 0) {
      await Future.delayed(const Duration(milliseconds: 100));
    }
  }

  stdout.writeln('\nDone! Downloaded: $downloaded, Failed: $failed');
}
